  beforeUnmount() {
    this.stopAutoRefresh()
  },
  methods: {
    async loadProjectData() {
      try {
        this.loading = true
        this.error = null
        
        // ‰ºòÂÖà‰ΩøÁî®props‰º†ÂÖ•ÁöÑproduct
        if (this.product) {
          this.model = this.product
          this.lastRefreshTime = new Date()
