        }

        // Â∞ùËØï‰ªésessionStorageËé∑Âèñ
        try {
          const cache = sessionStorage.getItem('lastProduct')
          if (cache) {
            this.model = JSON.parse(cache)
            this.lastRefreshTime = new Date()
            return
          }
        } catch(e) {
          console.warn('Ëß£ÊûêÁºìÂ≠òÊï∞ÊçÆÂ§±Ë¥•:', e)
        }

        // ‰ªéURLÂèÇÊï∞Ëé∑ÂèñÈ°πÁõÆIDÂπ∂Ë∞ÉÁî®API
        const projectId = this.$route.params.id || this.$route.query.id
        if (projectId) {
          await this.fetchProjectFromAPI(projectId)
        }
      } catch (error) {
        this.error = 'Âä†ËΩΩÈ°πÁõÆÊï∞ÊçÆÂ§±Ë¥•'
        console.error('‚ùå DetailPage: Âä†ËΩΩÈ°πÁõÆÊï∞ÊçÆÂ§±Ë¥•:', error)
      } finally {
        this.loading = false
      }
    },

    async fetchProjectFromAPI(projectId) {
      try {
        console.log('üîÑ DetailPage: ‰ªéÊï∞ÊçÆÂ∫ìËé∑ÂèñÈ°πÁõÆÊï∞ÊçÆ...', projectId)
        
        const response = await productAPI.getProductByCode(projectId)
        
        if (response.status === 0) {
          // Êò†Â∞ÑÊï∞ÊçÆÂ∫ìÂ≠óÊÆµÂà∞ÂâçÁ´ØÊúüÊúõÁöÑÂ≠óÊÆµÂêç
          const rawData = response.data
          this.model = {
            ...rawData,
            totalOffering: rawData.total_token,
            subscribed: rawData.current_subscribed_token,
            targetYield: rawData.target_yield,
            ltv: rawData.LTV,
            annualInterestRate: rawData.annual_interest_rate,
            loanAmount: rawData.loan_amount,
            valuation: rawData.valuation,
            image: rawData.image || this.getProductImage(rawData.code)
          }
          this.lastRefreshTime = new Date()
          console.log('‚úÖ DetailPage: È°πÁõÆÊï∞ÊçÆÂä†ËΩΩÊàêÂäü:', this.model)
        } else {
          this.error = response.message || 'Ëé∑ÂèñÈ°πÁõÆÊï∞ÊçÆÂ§±Ë¥•'
          console.error('‚ùå DetailPage: APIËøîÂõûÈîôËØØ:', response)
        }
      } catch (error) {
        this.error = 'ÁΩëÁªúÈîôËØØÔºåÊó†Ê≥ïËé∑ÂèñÈ°πÁõÆÊï∞ÊçÆ'
        console.error('‚ùå DetailPage: Ëé∑ÂèñÈ°πÁõÆÊï∞ÊçÆÂ§±Ë¥•:', error)
      }
    },
    
    // Âà∑Êñ∞Êï∞ÊçÆ
    async refreshProjectData() {
      console.log('üîÑ DetailPage: ÊâãÂä®Âà∑Êñ∞È°πÁõÆÊï∞ÊçÆ...')
      await this.loadProjectData()
    },
    
    // ÂºÄÂßãËá™Âä®Âà∑Êñ∞
    startAutoRefresh() {
      // ÊØè30ÁßíËá™Âä®Âà∑Êñ∞‰∏ÄÊ¨°Êï∞ÊçÆ
      this.refreshInterval = setInterval(() => {
        console.log('üîÑ DetailPage: Ëá™Âä®Âà∑Êñ∞È°πÁõÆÊï∞ÊçÆ...')
        this.loadProjectData()
      }, 30000) // 30Áßí
    },
    
    // ÂÅúÊ≠¢Ëá™Âä®Âà∑Êñ∞
    stopAutoRefresh() {
      if (this.refreshInterval) {
        clearInterval(this.refreshInterval)
        this.refreshInterval = null
        console.log('‚èπÔ∏è DetailPage: ÂÅúÊ≠¢Ëá™Âä®Âà∑Êñ∞')
      }
    },
    
    // Ê†ºÂºèÂåñÊó∂Èó¥ÊòæÁ§∫
    formatTime(date) {
      if (!date) return ''
      const now = new Date()
      const diff = now - date
      const minutes = Math.floor(diff / 60000)
      
      if (minutes < 1) return 'ÂàöÂàö'
      if (minutes < 60) return `${minutes}ÂàÜÈíüÂâç`
      
      const hours = Math.floor(minutes / 60)
      if (hours < 24) return `${hours}Â∞èÊó∂Ââç`
      
      const days = Math.floor(hours / 24)
      return `${days}Â§©Ââç`
    },
    
    // ÊóßÊñπÊ≥ï‰øùÁïôÔºàÂ¶ÇÊûúÈúÄË¶ÅÔºâ
    async fetchProjectFromAPI_old(projectId) {
      this.loading = true
      this.error = null
      
      try {
        const response = await fetch(import.meta.env.VITE_API_Project_URL)
        const data = await response.json()
        
        if (data.status === 0 && data.data) {
          this.model = data.data
          // ÁºìÂ≠òÂà∞sessionStorage
          sessionStorage.setItem('lastProduct', JSON.stringify(data.data))
        } else {
          this.error = data.message || 'Ëé∑ÂèñÈ°πÁõÆËØ¶ÊÉÖÂ§±Ë¥•'
        }
      } catch (error) {
        console.error('APIËØ∑Ê±ÇÂ§±Ë¥•:', error)
        this.error = 'ÁΩëÁªúËØ∑Ê±ÇÂ§±Ë¥•ÔºåËØ∑Á®çÂêéÈáçËØï'
      } finally {
        this.loading = false
      }
    },

    // Ëé∑ÂèñÊèêÂâçËøòÊ¨æ‰ø°ÊÅØ
    getPrepaymentInfo() {
      if (!this.model?.loanTerm) return '-'
      const term = this.model.loanTerm.toLowerCase()
      if (term.includes('6‰∏™Êúà') || term.includes('6 months')) {
        return 'Available after 6 months'
      }
      return 'Not available'
    },

    // Ëé∑ÂèñTotal TokenÊï∞ÈáèÔºà‰∏éLoan Amount‰øùÊåÅ‰∏ÄËá¥Ôºâ
    getTotalToken() {
      if (!this.model?.loanAmount) return '-'
      
      // ‰ªéloanAmount‰∏≠ÊèêÂèñÊï∞Â≠óÔºå‰æãÂ¶Ç "AUD 1,000,000" -> 1000000
      const loanAmountStr = this.model.loanAmount.toString()
      const numbers = loanAmountStr.match(/[\d,]+/g)
      
      if (numbers && numbers.length > 0) {
        // ÂèñÁ¨¨‰∏Ä‰∏™Êï∞Â≠óÔºåÁßªÈô§ÈÄóÂè∑
        const amount = numbers[0].replace(/,/g, '')
        // ËΩ¨Êç¢‰∏∫Êï∞Â≠óÂπ∂Ê†ºÂºèÂåñ‰∏∫tokenÊï∞Èáè
        const tokenAmount = parseInt(amount)
        
        // Â¶ÇÊûúloan amountÂæàÂ§ßÔºåÂèØËÉΩÈúÄË¶ÅËΩ¨Êç¢‰∏∫Êõ¥Â∞èÁöÑtokenÂçï‰Ωç
        // ‰æãÂ¶ÇÔºö1,000,000 AUD -> 1,000,000 tokens (1:1ÊØî‰æã)
        return `${tokenAmount.toLocaleString()} tokens`
      }
      
      return '-'
    },

    // Ëé∑Âèñ‰∫ßÂìÅÂõæÁâá
    getProductImage(code) {
      const imageMap = {
        'RWA001': '/pics/TYMU.png',
        'RWA002': '/pics/SQNB.png',
        'RWA003': '/pics/LZYT.png',
        'YYD': '/pics/YYD.png',
        'COMP': '/pics/TYMU.png'
      }
      return imageMap[code] || '/pics/TYMU.png'
    },

    // Ê†ºÂºèÂåñÊï∞Â≠ó
    formatNumber(value) {
      if (!value) return '0'
      const num = parseFloat(value)
      if (isNaN(num)) return value
      return num.toLocaleString()
    },

    // Ê†ºÂºèÂåñÊó•Êúü
    formatDate(dateString) {
      if (!dateString) return '-'
      try {
        const date = new Date(dateString)
        return date.toLocaleDateString('en-AU', {
          year: 'numeric',
          month: 'long',
          day: 'numeric'
        })
      } catch (error) {
        return dateString
      }
    },

    // Â§ÑÁêÜÊäïËµÑÊåâÈíÆÁÇπÂáª
    handleInvest() {
      if (!this.model) {
        console.error('No project data available')
        return
      }
      
      // Ëé∑ÂèñÈ°πÁõÆ‰ª£Á†ÅÔºå‰ºòÂÖà‰ΩøÁî®codeÂ≠óÊÆµÔºåÁÑ∂ÂêéÊòØproject_id
      const projectCode = this.model.code || this.model.project_id || this.model.projectCode
      
      if (!projectCode) {
        console.error('No project code found')
        return
      }
      
      console.log('üöÄ Ë∑≥ËΩ¨Âà∞‰∫§ÊòìÈ°µÈù¢ÔºåÈ°πÁõÆ‰ª£Á†Å:', projectCode)
      
      // Ë∑≥ËΩ¨Âà∞‰∫§ÊòìÈ°µÈù¢Ôºå‰ΩøÁî®Ê≠£Á°ÆÁöÑË∑ØÁî±Ê†ºÂºè
      this.$router.push({
        name: 'tradeProject',
        params: { code: projectCode }
      })
    }
  },
  
  watch: {
    // ÁõëÂê¨Ë∑ØÁî±ÂèòÂåñÔºåÈáçÊñ∞Âä†ËΩΩÊï∞ÊçÆ
    '$route'(to, from) {
      if (to.params.id !== from.params.id) {
        this.loadProjectData()
      }
    },
    
    // ÁõëÂê¨propsÂèòÂåñ
    product: {
      handler(newProduct) {
        if (newProduct) {
          this.model = newProduct
        }
      },
      immediate: true
    }
  }
}
